			                                6. Suponha que $f$ seja uma função com a propriedade $|f(x)|\leq x^2$ para todo $x$. Mostre que $f(0)=0$. A seguir, mostre que $f'(0)=0$.
			                            </p>
			                            <!-- Resposta -->
			                            <p style="display: none; background: #eee; padding: 0.5cm " id="d_6_">
			                                Dica:
			                                <br />
			                                Note que o módulo de um número real $a$ pode ser dado por $|a|=\max \{-a,a\}$ (o módulo de $a$ é o maior número entre $a$ e $-a$). Assim, $-a\leq |a|$ e $a\leq |a|$. Consequentemente, tem-se que $|x|\leq a$ se, e somente se, $-a\leq x\leq a$.
			                            </p>
			                            <p style="display: none; background: #eee; padding: 0.5cm " id="r_6_">
			                                Solução:
			                                <br />
			                                Por hipótese, $|f(0)|\leq 0\Rightarrow -0\leq f(0)\leq 0$, ou seja, $f(0)=0$. Além disso, $-x^2\leq f(x)\leq x^2$ para todo $x$, logo -x\leq \frac{f(x)}{x}\leq x$ para todo $x$ e, pelo Teorema do Confronto, $\lim_{x\to0}\frac{f(x)}{x}$. Portanto, $f'(0)$.
			                            </p>
